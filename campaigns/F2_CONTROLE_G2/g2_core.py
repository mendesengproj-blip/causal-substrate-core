def _lin_solve_subspace(T, row_constraints):
    """Vectors in span(T) whose matrices satisfy the given linear constraints.
    row_constraints: callable M -> flat residual array."""
    dimA = T.shape[0]
    rows = []
    probe = [row_constraints(T[a]) for a in range(dimA)]
    M = np.array([np.asarray(p).ravel() for p in probe]).T.real  # (ncon, dimA)
    Mi = np.array([np.asarray(p).ravel() for p in probe]).T.imag
    M = np.vstack([M, Mi])
    _, s, Vh = np.linalg.svd(M)
    ns = [Vh[r] for r in range(dimA) if (r >= len(s) or s[r] < 1e-10 * max(s))]
    return np.array(ns)                                        # coeff vectors


def so4_split(T):
    """S = {A in g2 : A preserves span(e1,e2,e3)} = so(4); split into the two
    su(2) ideals; return (Xs, Ys): X acts on the 3-block as so(3) (+ spin-1/2
    on the 4-block), Y acts only on the 4-block."""
    # constraint: lower-left 4x3 block of the (antisymmetric) matrix vanishes
    coeffs = _lin_solve_subspace(T, lambda M: M[3:, :3])
    S = np.einsum("ab,bij->aij", coeffs, T)                    # (dimS, 7, 7)
    dimS = S.shape[0]
    # kernel of the 3-block action = ideal Y (acts only on 4-part)
    M3 = np.array([S[a][:3, :3].ravel() for a in range(dimS)]).T
    M3 = np.vstack([M3.real, M3.imag])
    _, s3, Vh3 = np.linalg.svd(M3)
    ker = [Vh3[r] for r in range(dimS)
           if (r >= len(s3) or s3[r] < 1e-10 * max(s3))]
    Yc = np.array(ker)
    Y = np.einsum("ab,bij->aij", Yc, S)
    # X ideal = Tr-orthogonal complement of Y inside S
    G = np.einsum("aij,bji->ab", S, Y).real / 2.0              # overlaps
    # projector onto complement in coefficient space
    Q, _ = np.linalg.qr(Yc.T)
    P = np.eye(dimS) - Q @ Q.T
    Xc_raw = P
    # orthonormalize surviving directions
    u, sv, _ = np.linalg.svd(Xc_raw)
    Xc = u[:, sv > 1e-8].T
    X = np.einsum("ab,bij->aij", Xc, S)
    return X, Y


def _su2_triple(W, block=False):
    """From a 3-dim su(2) stack W (Hermitian), build (J1,J2,J3) with
    [J1,J2] = i J3 cyclic.
    block=True: align exactly with the standard spin-1 matrices
    (L_a)_{bc} = -i eps_{abc} in the upper 3x3 block (the block action is an
    algebra iso, so exact block alignment gives exact global closure).
    block=False: orthonormal basis + rescale by the structure constant."""
    assert W.shape[0] == 3
    eps = np.zeros((3, 3, 3))
    eps[0, 1, 2] = eps[1, 2, 0] = eps[2, 0, 1] = 1
    eps[0, 2, 1] = eps[1, 0, 2] = eps[2, 1, 0] = -1
    if block:
        Ls = [-1j * eps[a] for a in range(3)]                  # Hermitian L_a
        Wb = np.array([W[a][:3, :3] for a in range(3)])
        A = np.array([Wb[a].ravel() for a in range(3)]).T      # (9, 3)
        A = np.vstack([A.real, A.imag])
        Js = []
        for k in range(3):
            b = np.concatenate([Ls[k].ravel().real, Ls[k].ravel().imag])
            c, *_ = np.linalg.lstsq(A, b, rcond=None)
            Js.append(np.einsum("a,aij->ij", c, W))
        return np.array(Js)
    # orthogonalize to equal Tr-norm (invariant form => f_abc = c eps_abc)
    G = np.einsum("aij,bji->ab", W, W).real
    Linv = np.linalg.inv(np.linalg.cholesky(G))
    Wo = np.einsum("ab,bij->aij", Linv, W)                     # Tr(WaWb)=delta
    C12 = (Wo[0] @ Wo[1] - Wo[1] @ Wo[0]) / 1j
    c = float(np.trace(C12 @ Wo[2]).real / np.trace(Wo[2] @ Wo[2]).real)
    # [W1/c, W2/c] = (1/c^2)(i c W3) = i (W3/c)  => J_k = W_k / c
    return Wo / c


def diag_so3(T):
    """The diagonal so(3) of SO(4) in G2 (7 = 3+3+1): Hermitian triple
    (J1,J2,J3), [J1,J2] = i J3 cyclic; n.J has spectrum {+-1,+-1,0,0,0}."""
    X, Y = so4_split(T)
    JX = _su2_triple(X, block=True)
    # scale JX so that its 3-block equals the exact L_a (spin-1 unit): then
    # [JX1, JX2] = i JX3 automatically in the block; verify globally below.
    JY = _su2_triple(Y)
    # align signs: require [JY1,JY2] = +i JY3
    C = (JY[0] @ JY[1] - JY[1] @ JY[0]) / 1j
    if np.trace(C @ JY[2]).real < 0:
        JY[2] = -JY[2]
    # match normalization: JY spins are 1/2 on the 4-block; the triple from
    # _su2_triple already has unit structure constants.
    J = JX + JY
    return J, JX, JY


def su2_residual(J):
    """max_k || [J_k, J_{k+1}] - i J_{k+2} ||_max (cyclic)."""
    r = 0.0
    for k in range(3):
        C = J[k] @ J[(k + 1) % 3] - J[(k + 1) % 3] @ J[k] - 1j * J[(k + 2) % 3]
        r = max(r, float(np.max(np.abs(C))))
    return r


def g2_hedgehog(J, L, half_width=2.6, charge=+1, w_core=0.8):
    """G2 Skyrmion via the diagonal so(3): U = exp(i F(r) n.J), F = 2pi
    exp(-r/w) (F(0)=2pi is single-valued because all J-spins are integer).
    Predicted B_inf = Dynkin index = 4 (PRE_REGISTRO)."""
    x = np.linspace(-half_width, half_width, L)
    dx = float(x[1] - x[0])
    X, Yg, Z = np.meshgrid(x, x, x, indexing="ij")
    r = np.sqrt(X ** 2 + Yg ** 2 + Z ** 2)
    rs = np.where(r > 0, r, 1.0)
    nx, ny, nz = X / rs, Yg / rs, Z / rs
    nx[r == 0] = ny[r == 0] = nz[r == 0] = 0.0
    if charge < 0:
        nz = -nz
    F = 2.0 * PI * np.exp(-r / w_core)
    H = (nx[..., None, None] * J[0] + ny[..., None, None] * J[1]
         + nz[..., None, None] * J[2])
    U = sc.sun_exp(F[..., None, None] * H)
    return U, dx


