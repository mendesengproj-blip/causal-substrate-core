def goldstone_twists_stack(T, sampler, L=14):
    rng = np.random.default_rng(0)
    N = T.shape[-1]
    U0 = sampler(1, rng)[0]
    base = np.broadcast_to(U0, (L, L, L, N, N)).copy()
    E0 = sc.chiral_energy_grad(base, 1.0)
    x = np.arange(L)
    ks = [2 * PI * nk / L for nk in (1, 2, 3)]
    modes = {}
    n_gapless = 0
    for a in range(T.shape[0]):
        dEs, stiff = [], []
        for k in ks:
            Xtw = sc.sun_exp((k * x)[:, None, None] * T[a][None])
            U = np.einsum("aij,jk->aik", Xtw, U0)
            field = np.broadcast_to(U[:, None, None], (L, L, L, N, N)).copy()
            Et = sc.chiral_energy_grad(field, 1.0)
            dEs.append(float(Et - E0))
            stiff.append(float((Et - E0) / k ** 2))
        gapless = (dEs[0] > 0) and (dEs[0] < dEs[-1]) and (stiff[0] > 1e-6)
        n_gapless += int(gapless)
        modes[f"gen_{a}"] = {"gapless": bool(gapless), "dE": dEs}
    return {"expected": int(T.shape[0]), "found": n_gapless, "modes": modes}


