def _phi_tensor():
    phi = np.zeros((7, 7, 7))
    for (i, j, k) in FANO:
        i, j, k = i - 1, j - 1, k - 1
        for (a, b, c, s) in [(i, j, k, 1), (j, k, i, 1), (k, i, j, 1),
                             (j, i, k, -1), (i, k, j, -1), (k, j, i, -1)]:
            phi[a, b, c] = s
    return phi


def so7_basis():
    """21 antisymmetric E_ij - E_ji (i<j), Tr(A^T B) = 2 delta."""
    basis = []
    for i in range(7):
        for j in range(i + 1, 7):
            A = np.zeros((7, 7))
            A[i, j] = 1.0
            A[j, i] = -1.0
            basis.append(A)
    return np.array(basis)


def g2_generators():
    """(14, 7, 7) Hermitian stack T_a = -i A_a, Tr(T_a T_b) = 2 delta_ab."""
    phi = _phi_tensor()
    basis = so7_basis()
    # constraint rows: for each i, sum_jk phi[i,j,k] A[j,k] = 0
    M = np.einsum("ijk,bjk->ib", phi, basis)      # (7, 21)
    _, s, Vh = np.linalg.svd(M)
    null_mask = np.zeros(21, bool)
    null_mask[len(s):] = True                      # rows beyond rank
    tol = 1e-10 * max(s) if len(s) else 1.0
    nvecs = [Vh[r] for r in range(21) if (r >= len(s) or s[r] < tol)]
    coeffs = np.array(nvecs)                       # orthonormal in coeff space
    A = np.einsum("ab,bij->aij", coeffs, basis)    # Tr(A_a^T A_b) = 2 delta
    T = -1j * A.astype(complex)
    return T, phi


def haar_walk(T, n, rng, steps=60, scale=1.0):
    """~Haar samples on the compact group generated by stack T: product of
    `steps` exponentials with Gaussian coordinates (random walk mixes)."""
    dimA = T.shape[0]
    N = T.shape[-1]
    U = np.broadcast_to(np.eye(N, dtype=complex), (n, N, N)).copy()
    for _ in range(steps):
        phi = scale * rng.standard_normal((n, dimA))
        U = sc.sun_from_coords(T, phi) @ U
    return U


