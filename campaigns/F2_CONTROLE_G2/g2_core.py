def gauge_init_stack(sampler, N, L, rng, hot=True):
    shape = (4, L, L, L, L)
    if hot:
        return sampler(int(np.prod(shape)), rng).reshape(shape + (N, N))
    return np.broadcast_to(np.eye(N, dtype=complex), shape + (N, N)).copy()


def gauge_metropolis_sweep_stack(U, beta, rng, step, T, n_hit=2):
    N = U.shape[-1]
    dimA = T.shape[0]
    L = U.shape[1]
    g = np.arange(L)
    I, Jm, K, Tt = np.meshgrid(g, g, g, g, indexing="ij")
    parity = (I + Jm + K + Tt) % 2
    acc_tot = cnt_tot = 0
    for mu in range(4):
        A = sc.staple_sum(U, mu)
        for par in (0, 1):
            mask = parity == par
            for _ in range(n_hit):
                Uold = U[mu][mask]
                Amask = A[mask]
                R = sc.sun_from_coords(
                    T, step * rng.standard_normal((Uold.shape[0], dimA)))
                Uprop = R @ Uold
                dtr = np.real(np.trace((Uprop - Uold) @ sc.dagger(Amask),
                                       axis1=-2, axis2=-1))
                dS = -(beta / N) * dtr
                p = np.exp(-np.clip(dS, 0.0, 50.0))
                a = rng.random(Uold.shape[0]) < p
                blk = U[mu][mask]
                blk[a] = Uprop[a]
                U[mu][mask] = blk
                acc_tot += int(a.sum())
                cnt_tot += a.size
    return acc_tot / max(cnt_tot, 1)


